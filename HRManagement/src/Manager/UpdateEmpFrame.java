    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        
        int email_flag=0;
        int pass_flag=0;
        int phno_flag=0;
        try{
        String name,email,street,city,state,country;
        long phno;
        String empid,mid,pid;
        int deptno;

        name=nameTxt.getText();
        email=emailTxt.getText();
        if(!isEmailValid(email)){
            JOptionPane.showMessageDialog(null, "Invalid Email ID", "Error", JOptionPane.ERROR_MESSAGE);
            email_flag=1;
        }

        phno=Long.parseLong(phnoTxt.getText());
        String phno_str=Long.toString(phno);
        if(phno_str.length()!=10){
            JOptionPane.showMessageDialog(null, "Invalid Phone no.", "Error", JOptionPane.ERROR_MESSAGE);
            phno_flag=1;
        }

        street=streetTxt.getText();
        city=cityTxt.getText();
        state=stateTxt.getText();
        country=countryTxt.getText();

        empid=eidTxt.getText();


        deptno=Integer.parseInt(deptTxt.getText());
        mid=managerTxt.getText();
        pid=projectTxt.getText();

        if(email_flag==0 && pass_flag==0 && phno_flag==0){
            FetchEmployees fup=new FetchEmployees();
            int status=fup.update(empid, name, email, phno, street, city, state, country, deptno, mid, pid);
            if(status==1){
                JOptionPane.showMessageDialog(null, "Record updated successfully", "Updation", JOptionPane.PLAIN_MESSAGE);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(null, "Record Updation Failed", "Updation", JOptionPane.ERROR_MESSAGE);
            }
        }

        }catch(NullPointerException ex){
            JOptionPane.showMessageDialog(null, "Enter all fields corectly", "Status", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_updateBtnActionPerformed

